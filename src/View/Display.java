    private void butAnimationTruckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butAnimationTruckActionPerformed
        // TODO add your handling code here:
        panel2D.setVisible(false);
        panel3D.setVisible(false);
        panelObj.setVisible(false);
        butPlayStop.setEnabled(true);
        panelAni.setVisible(true);
        
        if(running){
            ani.play(panelDrawingArea, panelDrawingArea.getGraphics(), angle, speed, 0);
            angle+= 30;
            flag +=10;
            if(flag > panelDrawingArea.getWidth()){
                time.stop();
                running = false;
                changePlayStop();
            }
        }else{
            ani.init(panelDrawingArea);
            ani.play(panelDrawingArea, panelDrawingArea.getGraphics(), 0, 0, 0);
        }
        lbCoAni.setText(ani.showCoor());
    }//GEN-LAST:event_butAnimationTruckActionPerformed

    private void butPlayStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butPlayStopActionPerformed
        // TODO add your handling code here:
        if(running==false){
            running = true;
            speed= 10;
            if(flag > panelDrawingArea.getWidth()){
                ani.init(panelDrawingArea);
                flag =0;
            }
            time = new Timer(delay, butAnimationTruck.getActionListeners()[0]);
            time.start();
        }else if (running){
            running = false;
            time.stop();
        }
        changePlayStop();
    }//GEN-LAST:event_butPlayStopActionPerformed
    void changePlayStop() {
        if (running) {
            butPlayStop.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Icon/stop.png")));
        } else if (!running) {
            butPlayStop.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Icon/play.png")));
        }
    }
