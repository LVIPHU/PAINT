    @Override
    public void paint(Graphics g) {
        super.paint(g); //To change body of generated methods, choose Tools | Templates.
        if(cbGrid.isSelected()){
            new Coordinate().drawGrid(panelDrawingArea, panelDrawingArea.getGraphics());
        }
        if(cbCoordinate.isSelected() && rb2D.isSelected()){
            new Coordinate().drawCoor(panelDrawingArea, panelDrawingArea.getGraphics());
        }else if(cbCoordinate.isSelected() && rb3D.isSelected()){
            new Coordinate3D().drawCoor(panelDrawingArea, panelDrawingArea.getGraphics());
        }
        
        // Vẽ hình 2d
        if(constraint2D() == true){
            if(butLine.isSelected()){
                if (cbCustomLine.getSelectedItem().toString().equals("DEFAULT")) {
                    line = new Line();
                    line.init(start, end, size, color);
                    line.draw(panelDrawingArea.getGraphics());
                    line.Axis(start, end, panelDrawingArea.getGraphics());
                    paint2D.add(line);
                } else if (cbCustomLine.getSelectedItem().toString().equals("DASH")) {
                    line = new Line();
                    line.init(start, end, size, color);
                    line.dashedLine(panelDrawingArea.getGraphics());
                    line.Axis(start, end, panelDrawingArea.getGraphics());
                    paint2D.add(line);
                } else if (cbCustomLine.getSelectedItem().toString().equals("DOT")) {
                    line = new Line();
                    line.init(start, end, size, color);
                    line.dotLine(panelDrawingArea.getGraphics());
                    line.Axis(start, end, panelDrawingArea.getGraphics());
                    paint2D.add(line);
                } else if (cbCustomLine.getSelectedItem().toString().equals("DASHDOT")) {
                    line = new Line();
                    line.init(start, end, size, color);
                    line.dashedDotLine(panelDrawingArea.getGraphics());
                    line.Axis(start, end, panelDrawingArea.getGraphics());
                    paint2D.add(line);
                } else if (cbCustomLine.getSelectedItem().toString().equals("DASHDOTDOT")) {
                    line = new Line();
                    line.init(start, end, size, color);
                    line.dashedDotDotLine(panelDrawingArea.getGraphics());
                    line.Axis(start, end, panelDrawingArea.getGraphics());
                    paint2D.add(line);
                } else if (cbCustomLine.getSelectedItem().toString().equals("ARROW")) {
                    line = new Line();
                    line.init(start, end, size, color);
                    line.arrowLine(panelDrawingArea.getGraphics());
                    line.Axis(start, end, panelDrawingArea.getGraphics());
                    paint2D.add(line);
                }
            }else if(butRectangle.isSelected()){
                rect = new Rectangle();
                rect.init(start, end, size, color);
                rect.draw(panelDrawingArea.getGraphics());
                rect.Axis(start,end,panelDrawingArea.getGraphics());
                paint2D.add(rect);
            }else if(butCircle.isSelected()){
                circle = new Circle();
                circle.init(start, end, size, color);
                circle.draw(panelDrawingArea.getGraphics());
                circle.Axis(panelDrawingArea.getGraphics()); 
                paint2D.add(circle);
            }else if(butTriangle.isSelected()){
                triangle = new Triangle();
                triangle.init(end, start, size, color);
                triangle.draw(panelDrawingArea.getGraphics());
                triangle.Axis(start,end,panelDrawingArea.getGraphics());
                paint2D.add(triangle);                
            }else if(butEllipse.isSelected()){
                elip = new Ellipse();
                elip.init(start, end, size, color);
                elip.draw(panelDrawingArea.getGraphics());
                elip.Axis(panelDrawingArea.getGraphics());
                paint2D.add(elip);                
            }
        }
        
        // Vẽ vật
        if(constraintObj()){
            if(object.equals("Truck")){
                if (butMove.isSelected()) {
                    t.drawTran(panelDrawingArea.getGraphics(),Integer.parseInt(tfNewX.getText())*size, -Integer.parseInt(tfNewY.getText())*size);
                } else if (butScaling.isSelected()) {
                    t.drawScale(panelDrawingArea.getGraphics(), Float.parseFloat(tfNewX.getText()), Float.parseFloat(tfNewY.getText()));
                } else if (butRotation.isSelected()) {
                    t.drawRotate(panelDrawingArea.getGraphics(), Float.parseFloat(tfNewCorner.getText()));;
                }
                lbHienThiThongTin.setText(t.showCoor());
            }else if(object.equals("Windmill")){
                if (butMove.isSelected()) {
                    wm.drawTran(panelDrawingArea.getGraphics(),Integer.parseInt(tfNewX.getText()), Integer.parseInt(tfNewY.getText()));
                } else if (butScaling.isSelected()) {
                    wm.drawScale(panelDrawingArea.getGraphics(), Float.parseFloat(tfNewX.getText()), Float.parseFloat(tfNewY.getText()));
                } else if (butRotation.isSelected()) {
                    wm.drawRotate(panelDrawingArea.getGraphics(), Float.parseFloat(tfNewCorner.getText()));;
                }
                lbHienThiThongTin.setText(wm.showCoor());
            }
        }else{
            if (object.equals("Truck")) {
                t.init();
                t.draw(panelDrawingArea.getGraphics());
            } else if (object.equals("Windmill")) {
                wm.init();
                wm.draw(panelDrawingArea.getGraphics());
            }
        }
        
        if(butReflX.isSelected()){
            if(object.equals("Truck")){
                t.drawRefX(panelDrawingArea.getGraphics());
                bgObject.clearSelection();
            }else if(object.equals("Windmill")){
                wm.drawRefX(panelDrawingArea.getGraphics());
                bgObject.clearSelection();
            }
        } else if (butReflY.isSelected()) {
            if(object.equals("Truck")){
                t.drawRefY(panelDrawingArea.getGraphics());
                bgObject.clearSelection();
            }else if(object.equals("Windmill")){
                wm.drawRefY(panelDrawingArea.getGraphics());
                bgObject.clearSelection();
            }
        }else if (butReflO.isSelected()) {
            if(object.equals("Truck")){
                t.drawRefO(panelDrawingArea.getGraphics());
                bgObject.clearSelection();
            }else if(object.equals("Windmill")){
                wm.drawRefO(panelDrawingArea.getGraphics());
                bgObject.clearSelection();
            }
        }
        
        // Vẽ hình 3d
        Graphics2D g2d = (Graphics2D) panelDrawingArea.getGraphics();
        if (constraint3D() == true) {
            if (butCube.isSelected()) {
                HinhHop hh = new HinhHop();
                hh.setColor(color);
                hh.setX(Integer.parseInt(tf3D_X.getText()));
                hh.setY(Integer.parseInt(tf3D_y.getText()));
                hh.setZ(Integer.parseInt(tf3D_Z.getText()));
                hh.setDai(Integer.parseInt(tf3D_Dai.getText()));
                hh.setRong(Integer.parseInt(tf3D_Rong.getText()));
                hh.setCao(Integer.parseInt(tf3D_Cao.getText()));
                hh.draw(g2d);
            } else if (butSphere.isSelected()) {
                HinhCau hc = new HinhCau();
                hc.setColor(color);
                hc.setX(Integer.parseInt(tf3D_X.getText()));
                hc.setY(Integer.parseInt(tf3D_y.getText()));
                hc.setZ(Integer.parseInt(tf3D_Z.getText()));
                hc.setR(Integer.parseInt(tf3D_Dai.getText()));
                hc.draw(g2d);
            } else if (butCone.isSelected()) {
                HinhNon hn = new HinhNon();
                hn.setColor(color);
                hn.setX(Integer.parseInt(tf3D_X.getText()));
                hn.setY(Integer.parseInt(tf3D_y.getText()));
                hn.setZ(Integer.parseInt(tf3D_Z.getText()));
                hn.setR(Integer.parseInt(tf3D_Dai.getText()));
                hn.setCao(Integer.parseInt(tf3D_Cao.getText()));
                hn.draw(g2d);
            }
        }
    }
    
